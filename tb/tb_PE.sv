    endproperty
    assert_A3: assert property (p_A3_scale_ready_window) 
        else $error("G9.A3 FAILED: pe_ready did not exhibit a 3 to 5 cycle low window following SCALE.");

    property p_A4_no_x_states;
        @(posedge clk) disable iff (!rst || post_rst_cnt < 3'd3) 
        (!$isunknown(data_out) && !$isunknown(valid_output));
    endproperty
    assert_A4: assert property (p_A4_no_x_states) 
        else $error("G9.A4 FAILED: 'X' or 'Z' state detected on data_out or valid_output after reset initialization.");

    property p_A5_reset_behavior;
        @(posedge clk) 
        !rst |-> (pe_ready == 1'b1) && (valid_output == 1'b0) && (data_out == 8'h00);
    endproperty
    assert_A5: assert property (p_A5_reset_behavior) 
        else $error("G9.A5 FAILED: Incorrect asynchronous output states while reset was asserted.");

    property p_A6_chp_slct_gating;
        @(posedge clk) disable iff (!rst) 
        // Relaxed: Ensure chp_slct wasn't low for BOTH of the previous cycles
        (!$past(chp_slct, 1) && !$past(chp_slct, 2)) |-> !valid_output;
    endproperty
    assert_A6: assert property (p_A6_chp_slct_gating) 
        else $error("G9.A6 FAILED: valid_output asserted despite chp_slct being low on the prior 2 cycles.");

