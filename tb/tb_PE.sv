    // G10. Lightly Randomized Smoke
    // ------------------------------------------------------------------------
    task test_G10_randomized_smoke();
        logic [4:0] r_opcode;
        logic r_chp_slct, r_valid_op, r_valid_A, r_valid_B;
        logic [31:0] r_A, r_B;
        
        $display("\n--- Starting Test Group G10: Randomized Smoke (2,000 Cycles) ---");
        
        drive_rst_acc(); predictor.reset_state();

        for (int i = 0; i < 2000; i++) begin
            int rand_val;
            rand_val = $urandom_range(1, 100);
            if      (rand_val <= 40) r_opcode = OP_MAC;           // 40%
            else if (rand_val <= 60) r_opcode = OP_ADD_BIAS;      // 20%
            else if (rand_val <= 68) r_opcode = OP_SCALE;         // 8%
            else if (rand_val <= 76) r_opcode = OP_EXEC_PP;       // 8%
            else if (rand_val <= 86) r_opcode = OP_READ_ACC_BYTE; // 10%
            else if (rand_val <= 90) r_opcode = OP_READ_CFG;      // 4%
            else if (rand_val <= 94) r_opcode = OP_RST_ACC;       // 4%
            else if (rand_val <= 98) r_opcode = OP_LOAD_CFG;      // 4%
            else                     r_opcode = OP_NOP;           // 2%

            r_chp_slct = ($urandom_range(1, 100) <= 90);
            r_valid_op = ($urandom_range(1, 100) <= 90);
            r_valid_A  = ($urandom_range(1, 100) <= 90);
            r_valid_B  = ($urandom_range(1, 100) <= 90);
            r_A        = $urandom();
            r_B        = $urandom();

            @(negedge clk);
            chp_slct     = r_chp_slct;
            valid_opcode = r_valid_op;
            pe_opcode    = r_opcode;
            valid_A      = r_valid_A;
            operand_A    = r_A;
            valid_B      = r_valid_B;
            operand_B    = r_B;

            @(posedge clk);
            
            if (r_chp_slct && r_valid_op && pe_ready) begin
                case (r_opcode)
                    OP_RST_ACC:       predictor.update_rst_acc();
                    OP_MAC:           if(r_valid_A && r_valid_B) predictor.update_mac(r_A, r_B);
                    OP_ADD_BIAS:      if(r_valid_B) predictor.update_add_bias(r_B);
                    OP_LOAD_CFG:      if(r_valid_A) predictor.config_reg = r_A[3:0];
                    OP_EXEC_PP:       predictor.update_exec_pp();
                    OP_SCALE:         begin
                                        if(r_valid_A && r_valid_B) predictor.update_scale(r_A, r_B);
                                      end
                    OP_READ_CFG:      predictor.data_out_reg = {4'h0, predictor.config_reg};
                    OP_READ_ACC_BYTE: ; 
                endcase
            end

            @(negedge clk);
            chp_slct = 1'b0; valid_opcode = 1'b0; valid_A = 1'b0; valid_B = 1'b0;

            #1;
            if (valid_output === 1'b1) begin
                if (data_out !== predictor.data_out_reg) begin
                    report_result($sformatf("G10 Random Issue (Cycle %0d, Op=%0d)", i, r_opcode), 1'b0, 
                                  $sformatf("DUT: %h, Exp: %h", data_out, predictor.data_out_reg));
                end
            end
            
            if (r_chp_slct && r_valid_op && pe_ready && r_opcode == OP_SCALE) begin
                wait_ready();
            end
        end
        report_result("G10 Lightly Randomized Smoke", 1'b1);
    endtask

