        input logic [4:0] op, 
        input logic v_a, 
        input logic [31:0] a, 
        input logic v_b, 
        input logic [31:0] b
    );
        @(negedge clk);
        chp_slct     = 1'b1;
        valid_opcode = 1'b1;
        pe_opcode    = op;
        valid_A      = v_a;
