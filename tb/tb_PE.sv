`timescale 1ns / 1ps

module tb_PE;

    // ========================================================================
    // 1. Signals and Clock/Reset Generation
    // ========================================================================
    logic clk;
    logic rst;

    // External instruction interface
    logic chp_slct;
    logic valid_opcode;
    logic [4:0] pe_opcode;

    // External operand interface
    logic valid_A;
    logic valid_B;
    logic [31:0] operand_A;
    logic [31:0] operand_B;

    // External outputs
    logic [7:0] data_out;
    logic pe_ready;
    logic valid_output;

    // 100 MHz Clock (10 ns period) -> 5ns high, 5ns low
    initial begin
        clk = 0;
        rst = 1'b0;
        forever #5 clk = ~clk;
    end

    // ========================================================================
    // 2. Opcode Definitions 
    // ========================================================================
    localparam logic [4:0] OP_NOP           = 5'd0;
    localparam logic [4:0] OP_RST_ACC       = 5'd1;
    localparam logic [4:0] OP_MAC           = 5'd2;
    localparam logic [4:0] OP_ADD_BIAS      = 5'd3;
    localparam logic [4:0] OP_SCALE         = 5'd4;
    localparam logic [4:0] OP_LOAD_CFG      = 5'd5;
    localparam logic [4:0] OP_EXEC_PP       = 5'd6;
    localparam logic [4:0] OP_READ_ACC_BYTE = 5'd7;
    localparam logic [4:0] OP_READ_CFG      = 5'd8;

    wire is_output_cmd = valid_opcode && chp_slct && 
                         (pe_opcode == OP_EXEC_PP || 
                          pe_opcode == OP_READ_ACC_BYTE || 
                          pe_opcode == OP_READ_CFG);

    logic [2:0] post_rst_cnt;
    always_ff @(posedge clk or negedge rst) begin
        if (!rst) begin
            post_rst_cnt <= 3'd0;
        end else if (post_rst_cnt < 3'd4) begin
            post_rst_cnt <= post_rst_cnt + 1;
        end
    end

    // ========================================================================
    // 3. DUT Instantiation
    // ========================================================================
    PE uut (
        .clk          (clk),
        .rst          (rst),
        .chp_slct     (chp_slct),
        .valid_opcode (valid_opcode),
        .pe_opcode    (pe_opcode),
        .valid_A      (valid_A),
        .valid_B      (valid_B),
        .operand_A    (operand_A),
        .operand_B    (operand_B),
        .data_out     (data_out),
        .pe_ready     (pe_ready),
        .valid_output (valid_output)
    );

    // ========================================================================
    // 4. The Golden Model Predictor Class
    // ========================================================================
    class pe_predictor;
        logic signed [63:0] accumulator;
        logic [3:0]         config_reg;
        logic [5:0]         shamt_reg;
        logic [7:0]         data_out_reg;

        function new();
            reset_state();
        endfunction

        function void reset_state();
            accumulator  = 64'sd0;
            config_reg   = 4'b0000;
            shamt_reg    = 6'b0;
            data_out_reg = 8'b0;
        endfunction
        
        function void update_rst_acc();
            accumulator = 64'sd0;
        endfunction

        function void update_add_bias(logic [31:0] opB);
            accumulator += $signed(opB); 
        endfunction

        function void update_mac(logic [31:0] opA, logic [31:0] opB);
            if (config_reg[3] == 1'b1) begin 
                for (int i = 0; i < 4; i++) begin
                    logic signed [7:0] a_val = opA[i*8 +: 8];
                    logic signed [7:0] b_val = opB[i*8 +: 8];
                    accumulator += (a_val * b_val); 
                end
            end else begin 
                for (int i = 0; i < 8; i++) begin
                    logic signed [3:0] a_val = opA[i*4 +: 4];
                    logic signed [3:0] b_val = opB[i*4 +: 4];
                    accumulator += (a_val * b_val);
                end
            end
        endfunction

        function void update_scale(logic [31:0] opA, logic [31:0] opB);
            logic signed [31:0] acc_lower = accumulator[31:0];
            logic signed [32:0] scale_unsigned_padded = {1'b0, opB}; 
            accumulator = acc_lower * scale_unsigned_padded;
            shamt_reg = opA[5:0];
        endfunction

        function void update_exec_pp();
            logic signed [63:0] shifted_val;
            logic signed [63:0] rounded_val;
            
            if (shamt_reg == 0) begin
                rounded_val = accumulator;
            end else begin
                shifted_val = accumulator >>> shamt_reg;
                rounded_val = shifted_val + ((accumulator >> (shamt_reg - 1)) & 1'b1);
            end

            data_out_reg = apply_activation(rounded_val, config_reg[2:0]);
        endfunction

        function logic [7:0] apply_activation(logic signed [63:0] rounded_val, logic [2:0] act_fn_sel);
            logic mode4x4 = (config_reg[3] == 1'b0); 
            
            if (mode4x4) begin
                logic signed [3:0] in_4;
                logic [3:0] out_4;
                
                // INT4 Saturation [-8, 7]
                if (rounded_val > 64'sd7) in_4 = 4'sd7;
                else if (rounded_val < -64'sd8) in_4 = -4'sd8;
                else in_4 = rounded_val[3:0];

                case (act_fn_sel)
                    3'b000: out_4 = in_4;                                             
                    3'b001: out_4 = in_4[3] ? 4'b0000 : in_4;                         
                    3'b010: out_4 = in_4[3] ? ($signed(in_4) >>> 6) : in_4;           
                    3'b011: out_4 = in_4[3] ? ($signed(in_4) >>> 2) : in_4;           
                    3'b100: out_4 = get_tanh_4(in_4);                                 
                    3'b101: out_4 = get_sigmoid_4(in_4);                              
                    3'b110: out_4 = get_gelu_4(in_4);                                 
                    3'b111: out_4 = get_silu_4(in_4);                                 
                    default: out_4 = 4'h0;
                endcase
                return {4'b0000, out_4}; 
            end else begin
                logic signed [7:0] in_8;
                logic [7:0] out_8;

                // INT8 Saturation [-128, 127]
                if (rounded_val > 64'sd127) in_8 = 8'sd127;
                else if (rounded_val < -64'sd128) in_8 = -8'sd128;
                else in_8 = rounded_val[7:0];

                case (act_fn_sel)
                    3'b000: out_8 = in_8;                                             
                    3'b001: out_8 = in_8[7] ? 8'h00 : in_8;                           
                    3'b010: out_8 = in_8[7] ? ($signed(in_8) >>> 6) : in_8;           
                    3'b011: out_8 = in_8[7] ? ($signed(in_8) >>> 2) : in_8;           
                    3'b100: out_8 = get_tanh_8(in_8);                                 
                    3'b101: out_8 = get_sigmoid_8(in_8);                              
                    3'b110: out_8 = get_gelu_8(in_8);                                 
                    3'b111: out_8 = get_silu_8(in_8);                                 
                    default: out_8 = 8'h00;
                endcase
                return out_8;
            end
        endfunction

