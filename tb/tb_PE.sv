        if (passed) begin
            $display("[PASS] %s", test_name);
            total_passes++;
        end else begin
            $error("[FAIL] %s - %s", test_name, error_msg);
            total_fails++;
        end
    endfunction

    task check_dout(string test_name);
        @(posedge clk);
        while (valid_output !== 1'b1) begin
            @(posedge clk);
        end

        if (data_out === predictor.data_out_reg) begin
            report_result(test_name, 1'b1);
        end else begin
            string msg;
            msg = $sformatf("DUT: %h, Expected: %h", data_out, predictor.data_out_reg);
            report_result(test_name, 1'b0, msg);
        end
    endtask

    task verify_accumulator(string test_name);
        logic [63:0] reconstructed_acc;
        
        for (int i = 0; i < 8; i++) begin
            drive_read_acc_byte(i[2:0]);
            
            @(posedge clk);
            while (valid_output !== 1'b1) @(posedge clk);
            
            reconstructed_acc[i*8 +: 8] = data_out;
        end

        if (reconstructed_acc === predictor.accumulator) begin
            report_result(test_name, 1'b1);
        end else begin
            string msg;
            msg = $sformatf("DUT ACC: %16h, Expected: %16h", reconstructed_acc, predictor.accumulator);
            report_result(test_name, 1'b0, msg);
        end
    endtask

